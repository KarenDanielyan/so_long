#include "libft.h"

/*
*	Reference: DNE
*
*	Description: Counts the number of nodes in a list.
*
*	Return Value: The length of the list.
*/
int	ft_lstsize(t_list *lst)
{
	int	size;

	size = 0;
	while (lst)
	{
		size++;
		lst = lst->next;
	}
	return (size);
}
